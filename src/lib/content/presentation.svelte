<h1 id="presentation">Presentation</h1>
<h2 id="summary">Summary</h2>
<p>
	My partner and me presented a chapter of the book “Factfulness” by Hans Rosling. The
	chapter focused on Experts and other sources of information. We spoke about how people
	with expertise in one specific field are often asked to comment or willingly comment on
	matters outside of there field. For example a neuroscientist speaking about fitness will
	often be treated as an expert in the field of fitness even though they have no education
	in that field. We then spoke about how people should look for experts who are clear
	about what they are experienced in and what falls outside of their domain. Experts who
	are honest about what they know are far more trustworthy than people who try to convince
	everyone that they are an “everything-expert”.
</p>
<h2 id="critical-comment">Critical Comment</h2>
<p>
	I though that our chapter’s topic was interesting and also that it was and still is an
	important topic. Many people who are experts in a field like medicine or some branch of
	physics often make unfounded claims about topics that they do not have any experience in
	(e.g. Politics) and their statements are believed to be true by many simply because they
	were said by an “expert”. This chapter showed a way to avoid falling for this kind of
	thinking.
</p>
<h2 id="fact-sheet">Fact Sheet</h2>
<h3 id="the-single-perspective-instinct">The Single Perspective Instinct</h3>
<h4 id="who-to-trust">Who to trust:</h4>
<p>
	• The media usually only presents sensational (mostly negative) information. • Experts
	should be trusted when other experts in there field agree with them and when they speak
	about a subject their expertise applies to.
</p>
<h4 id="hammers-and-nails">Hammers and Nails</h4>
<p>
	• People with deep knowledge about 1 area often try to apply the ways of thinking from
	that area to everything else.
</p>
<h4 id="ideas-and-ideologies">Ideas and Ideologies</h4>
<p>
	• The world is often more complicated than it might seem and big ideas claiming to
	answer everything or a lot of things usually don’t work in practice.
	<br />
	• Ideologies are a great way of inspiring change and uniting people but often are to general
	to work for everything.
	<br />
	• Problems should be addressed on a case by case bases with the help of experts in the problem
	domain.
</p>
<h4 id="data-and-interpretation">Data and Interpretation</h4>
<p>
	• Numbers and data aren’t useful on their own they have to be interpreted by a person.
	Not all data is useful in a specific situation, GDP or literacy isn’t always a good way
	of measuring how developed a country is.
</p>
