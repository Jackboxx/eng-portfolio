<h2 id="november-the-e-books-wars">November | The E-Books Wars</h2>
<p><a href="https://www.youtube.com/watch?v=2HIhuQsEW7k">Video</a></p>
<h3 id="summary-and-comment">Summary and Comment</h3>
<p>
	This episode discusses the struggle that has recently appeared within the book world,
	the struggle between libraries and publishers that was created by the popularity of
	E-Books. At first E-Books were treated like regular books by copyright law meaning a
	library could only lend one at a time and the library could only lend an E-Book a
	limited amount of time. But due to the increased ease of obtaining an E-Book publishers
	felt threatened and started to implement measures to prevent libraries from buying newly
	released E-Books. Further more, publishers also increased the price of their E-Book
	licenses and gave these licenses an expiration date.
</p>
<h3 id="feedback">Feedback</h3>
<p>
	I believe I will be saying this more then once but I think that this episode fails to
	address the root cause of the issues at hand. The episode is focused on how E-Books
	disrupted the balance of the book industry however it does not speak about the
	environment that allowed this problem to arise in the first place. The fact is that the
	goal of a library is to provide accessible knowledge to the common person but in
	contrast a publishers goal is to make money from an authors book. Notably, these goals
	are in conflict. If we lived in a world where knowledge was freely accessible to all
	publishers would not make money. Publishers fundamentally exist as a middle man between
	the author and the reader and the only reason this middle man is needed is because
	authors can’t financially support themselves otherwise. If we did not lived in a system
	that willingly or not mass produces starving artists but instead a system that supported
	the growth of creatives simply because their work is essential to a health human society
	we would not be having this issue. “Dare to dream of a flawed utopia”.
</p>
