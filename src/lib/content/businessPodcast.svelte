<h3 id="summary-and-comment-5">Summary and Comment</h3>
<p>
	This episode covers the ongoing protests regarding Puerto Rico’s beaches and how the
	dividing line that separates public beaches from private property should be drawn. The
	hosts discuss how the two major ways of defining a public beach in Puerto Rico clash and
