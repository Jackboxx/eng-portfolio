<h2 id="december-small-america-vs.-big-internet">
	December | Small America Vs. Big Internet
</h2>
<p><a href="https://www.youtube.com/watch?v=lR8Ow7W6it8">Video</a></p>
<h3 id="summary-and-comment-1">Summary and Comment</h3>
<p>
	This episodes covers the story of a town in the USA called Wilson that created their own
	fiber optic network. The town around 30,000 people didn’t receive quality internet from
	any private internet providers even after asking these providers to improve their
	infrastructure. So the town decided to create their own network and as soon as they
	started to be successful in this endeavor large telecom companies started lobbying at a
	state level to prevent city governments from being allowed to create local fiber
	networks. After a while the telecom companies succeeded but the town of Wilson was
	allowed to maintain their network as long as they didn’t expand it.
</p>
<h3 id="feedback-1">Feedback</h3>
<p>
	This episode was quite well written but again I feel like their is a critical aspect
	that should be present but is not. The situation is a text book indictment of modern
	capitalism. A small local city government wants to improve their peoples lives but as
	soon as they step on a large companies foot they are legally hounded until they stop. It
	should be obvious to anyone that a necessity like an internet connection should not be
	in the hands of the private sector and anyone who claims that giving something like the
	internet to the private sector will make it more efficient is directly proven wrong by
	this story. As the episode mentioned, the town Wilson managed to keep their internet and
	was described by the speaker as an oasis, a place with faster and cheaper internet than
	any telecom company was willing to provide. However, the reason why telecom companies
	did not provide internet infrastructure of the same quality as the town is not because
	they were not capable of doing so but because it would not have been profitable for
	them. The profit margin and the private sector kill innovation and reduce peoples well
	being. Further more, the internet itself would never have been created by any private
	company as building the necessary infrastructure would not have been profitable for
	decades (that is why it was only possible for the internet to come about due to research
	projects and military funding). The private sector only leeched off of the internet once
	they could extract a profit from it.
</p>
