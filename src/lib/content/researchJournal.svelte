<h3 id="job-satisfaction-trends">5. Job Satisfaction Trends</h3>
<p>
	As expected the general trend of job satisfaction (in the UK and Germany) is a downward
	trend [2]. This appears to be caused by an increase in the intensity of work given to
	workers and the reduction of autonomy in the work place. Adding to this effect is the
	fact that fewer people are working their desired amount of hours in a job. This effect
	isn’t only national it also effects individual businesses, as the job satisfaction
	inside a variety of business has decreased over the years. *It is work noting that the
	study on national trends (2) was published in 2005 so it is possible that trends have
	changed since then but the fact that article 1 sees a similar trend present in 2017
	makes this unlikely.
</p>
