<h3 id="deeper-theory">2. Deeper Theory</h3>
<p>
	Research into job satisfaction began in the 1930s and mainly focused on the nature of
	the job being worked and the relation ship with coworkers and supervisors. &gt;&gt; 2.
	“The assessment of job satisfaction through employee anonymous surveys became
	commonplace in the 1930s. Although prior to that time there was the beginning of
	interest in employee attitudes, there were only a handful of studies published.” In the
	following decades several models of job satisfaction emerged.
</p>
<h4 id="affect-theory">Affect Theory:</h4>
<p>
	This theory/model focuses on the dissonance with what a person has in there job and what
	they want from their job. In addition, how much a person values something determines how
	much they are effected when their job meats/doesn’t meats this value/expectation.
</p>
<h4 id="equity-theory">Equity Theory:</h4>
<p>
	This theory focuses on how a person perceives fairness in a company. In this theory a
	person evaluates the personal output (the effort put into the company) versus the input
	(the things gained from the company). A person also measures their output against the
	output other people are investing for a particular input to determine if the
	output-input ration is fair. There are other models that have been develop but these
	were the most interesting to me. One thing that is interesting to note is that the
	theories are not just applicable to job satisfaction but to general human behavior. In
	the next entry I’ll be looking through some surveys and studies to see how these
	theories match up with data.
</p>
